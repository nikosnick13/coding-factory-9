public class CharApp {

    public static void main(String[] args) {

        char ch1 = 'A';
        char ch2 = '*';
        char ch3 = 'k';

        System.out.printf("ch1 = %c, ordinal %d \n", ch1, (int)ch1);
        System.out.printf("ch2 = %c, ordinal %d \n", ch2, (int)ch2 );
        System.out.printf("ch3 = %c, ordinal %d", ch3, (int)ch3 );

    }

}
